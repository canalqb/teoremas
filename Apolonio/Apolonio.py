import math
import itertools

AB_values = [2**n for n in range(11)]
AC_values = [2**(n + 1) - 1 for n in range(11)]
BC_values = [2**n + (2**(n + 1) - 1) for n in range(11)]

combinacoes = itertools.product(AB_values, AC_values, BC_values)

for i, (AB, AC, BC) in enumerate(combinacoes, start=1):
    AM_squared = (2 * AB**2 + 2 * AC**2 - BC**2) / 4
    if AM_squared >= 0:
        AM = math.sqrt(AM_squared)
        print(f"Combinação {i}: AB = {AB}, AC = {AC}, BC = {BC}")
        print(f"  AM^2 = {AM_squared}")
        print(f"  AM (mediana) = {AM:.4f}")
        print("-" * 40)

